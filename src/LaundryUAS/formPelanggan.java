    private void Tampil_ID() {
    try {
       
        String sql ="SELECT id_pelanggan as a FROM pelanggan order by id_pelanggan desc";
        Statement st = cn.createStatement();
        ResultSet rs = st.executeQuery(sql);
             
            if (rs.next()) {
                String No_Urut = rs.getString("a");
                int a = Integer.parseInt(No_Urut);
                int panjang = No_Urut.length();
                for (int i = 0; i < 2 - panjang; i++) {
                    a = a;
                }
              
                tdId.setText(Integer.toString(a + 1));
            } else {
                tdId.setText("1");
                
            }
        rs.close();
    } catch (Exception e) {
    }
}
