    public void readData(String where){
        try{
            st =cn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs=st.executeQuery("SELECT * FROM pelanggan "+ where);
            
            while(rs.next()){
                Object[]data={
                  
                    rs.getString("id_pelanggan"),
                    rs.getString("nama"),
                    rs.getString("no_hp"),
                    rs.getString("tgl_terima"),
                    rs.getString("tgl_pengembalian"),
                    rs.getString("pilihan"),
                    rs.getString("layanan")
                    
                };
                
                tabModel.addRow(data);
            }
        
        }catch(Exception e){
            e.printStackTrace();
        }
    }
